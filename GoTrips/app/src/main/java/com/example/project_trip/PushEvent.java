package com.example.project_trip;

public class PushEvent {

    String name , name2 , name3;

    public PushEvent(String name, String name2, String name3) {
        this.name = name;
        this.name2 = name2;
        this.name3 = name3;
    }

    public String getName() {
        return name;
    }

    public String getName2() {
        return name2;
    }

    public String getName3() {
        return name3;
    }
}
